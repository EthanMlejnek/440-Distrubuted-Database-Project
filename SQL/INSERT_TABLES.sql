-- Core Tables
CREATE TABLE DEPARTMENT (
    department_id INT PRIMARY KEY NOT NULL, 
    department_name VARCHAR(255)
);

CREATE TABLE ROLE (
    role_id INT PRIMARY KEY NOT NULL,
    role_name VARCHAR(255)
);

CREATE TABLE PERSON (
    person_id INT PRIMARY KEY NOT NULL,
    first_name VARCHAR(255),
    last_name VARCHAR(255), 
    date_of_birth DATE
);

CREATE TABLE COUNTRY (
    country_id INT PRIMARY KEY NOT NULL,
    country_name VARCHAR(255) 
);

CREATE TABLE REGION_TYPE (
    region_type_id INT PRIMARY KEY NOT NULL,
    region_type VARCHAR(255) 
);

CREATE TABLE PROJECT (
    project_id INT PRIMARY KEY NOT NULL, 
    project_name VARCHAR(255) 
);

CREATE TABLE CONTRACT (
    contract_id INT PRIMARY KEY NOT NULL, 
    contract_max_hours INT,
    billing_rate DECIMAL(10, 2)
);

CREATE TABLE PHONE (
    phone_id INT PRIMARY KEY NOT NULL,
    phone_number VARCHAR(255),
    phone_type VARCHAR(255) 
);

CREATE TABLE EMAIL (
    email_id INT PRIMARY KEY NOT NULL,
    email VARCHAR(255),
    email_type VARCHAR(255)
);

CREATE TABLE INVOICE (
    invoice_id INT PRIMARY KEY NOT NULL,
    invoice_date DATE,
    invoice_status VARCHAR(255)
);

CREATE TABLE COMPANY (
    company_id INT PRIMARY KEY NOT NULL, 
    company_name VARCHAR(255),
    company_type VARCHAR(255) 
);

CREATE TABLE POLICY (
    policy_id INT PRIMARY KEY NOT NULL,
    policy_type VARCHAR(255)
);

CREATE TABLE BILLING (
    billing_id INT PRIMARY KEY NOT NULL,
    billed_hours INT,
    bill_rate DECIMAL(10, 2),
    billed_total DECIMAL(10, 2)
);

-- Start Tables with Foreign Keys
CREATE TABLE REGION (
    region_id INT PRIMARY KEY NOT NULL, 
    region_type_id INT, 
    region_name VARCHAR(255),

    FOREIGN KEY (region_type_id) REFERENCES REGION_TYPE(region_type_id)
);

CREATE TABLE EMPLOYEE (
    employee_id INT PRIMARY KEY NOT NULL,
    department_id INT,
    person_id INT, 
    hire_date DATE,

    FOREIGN KEY (department_id) REFERENCES DEPARTMENT(department_id),
    FOREIGN KEY (person_id) REFERENCES PERSON(person_id)
);

CREATE TABLE CLIENT (
    client_id INT PRIMARY KEY NOT NULL, 
    person_id INT,
    company_id INT, 

    FOREIGN KEY (person_id) REFERENCES PERSON(person_id),
    FOREIGN KEY (company_id) REFERENCES COMPANY(company_id) 
);

CREATE TABLE TIMECARD (
    timecard_id INT PRIMARY KEY NOT NULL, 
    project_id INT,
    country_id INT,
    hours_worked INT,

    FOREIGN KEY (project_id) REFERENCES PROJECT(project_id),
    FOREIGN KEY (country_id) REFERENCES COUNTRY(country_id) 
);

CREATE TABLE CITY (
    city_id INT PRIMARY KEY NOT NULL,
    city_name VARCHAR(255)
);

CREATE TABLE ADDRESS (
    address_id INT PRIMARY KEY NOT NULL,
    street_address VARCHAR(255),
    postal_code VARCHAR(255)
);

-- Line Item / Bridge Tables
CREATE TABLE ADDRESS_CITY (
    address_id INT,
    city_id INT,

    PRIMARY KEY (address_id, city_id),
    FOREIGN KEY (address_id) REFERENCES ADDRESS(address_id),
    FOREIGN KEY (city_id) REFERENCES CITY(city_id) 
);

CREATE TABLE CITY_REGION (
    city_id INT,
    region_id INT,

    PRIMARY KEY (city_id, region_id),
    FOREIGN KEY (city_id) REFERENCES CITY(city_id),
    FOREIGN KEY (region_id) REFERENCES REGION(region_id) 
);

CREATE TABLE BILLING_CONTRACT (
    billing_id INT,
    contract_id INT, 

    PRIMARY KEY (billing_id, contract_id),
    FOREIGN KEY (billing_id) REFERENCES BILLING(billing_id),
    FOREIGN KEY (contract_id) REFERENCES CONTRACT(contract_id) 
);

CREATE TABLE BILLING_INVOICE (
    billing_id INT,
    invoice_id INT, 

    PRIMARY KEY (billing_id, invoice_id),
    FOREIGN KEY (billing_id) REFERENCES BILLING(billing_id),
    FOREIGN KEY (invoice_id) REFERENCES INVOICE(invoice_id) 
);

CREATE TABLE CLIENT_INVOICE (
    client_id INT,
    invoice_id INT, 

    PRIMARY KEY (client_id, invoice_id),
    FOREIGN KEY (client_id) REFERENCES CLIENT(client_id),
    FOREIGN KEY (invoice_id) REFERENCES INVOICE(invoice_id) 
);

CREATE TABLE COUNTRY_POLICY (
    country_id INT,
    policy_id INT,
    max_weekly_hours INT, 

    PRIMARY KEY (country_id, policy_id),
    FOREIGN KEY (country_id) REFERENCES COUNTRY(country_id),
    FOREIGN KEY (policy_id) REFERENCES POLICY(policy_id)
);

CREATE TABLE CONTRACT_COUNTRY(
    contract_id INT,
    country_id INT,

    PRIMARY KEY (contract_id, country_id),
    FOREIGN KEY (contract_id) REFERENCES CONTRACT(contract_id),
    FOREIGN KEY (country_id) REFERENCES COUNTRY(country_id) 
);

CREATE TABLE EMPLOYEE_ROLE (
    employee_id INT,
    role_id INT,
    assigned_date DATE, 
    end_date DATE, 

    PRIMARY KEY (employee_id, role_id),
    FOREIGN KEY (employee_id) REFERENCES EMPLOYEE(employee_id),
    FOREIGN KEY (role_id) REFERENCES ROLE(role_id) 
);

CREATE TABLE EMPLOYEE_TIMECARD (
    employee_id INT,
    timecard_id INT,
    timecard_date DATE, 
    approval_status VARCHAR(255),

    PRIMARY KEY(employee_id, timecard_id),
    FOREIGN KEY (employee_id) REFERENCES EMPLOYEE(employee_id),
    FOREIGN KEY (timecard_id) REFERENCES TIMECARD(timecard_id) 
);

CREATE TABLE PERSON_ADDRESS (
    person_id INT, 
    address_id INT,
    address_type VARCHAR(255),

    PRIMARY KEY (person_id),
    FOREIGN KEY (person_id) REFERENCES PERSON(person_id),
    FOREIGN KEY (address_id) REFERENCES ADDRESS(address_id)
);

CREATE TABLE PERSON_PHONE (
    person_id INT, 
    phone_id INT,
    phone_type VARCHAR(255),
    is_primary VARCHAR(255),

    PRIMARY KEY (person_id, phone_id),
    FOREIGN KEY (person_id) REFERENCES PERSON(person_id),
    FOREIGN KEY (phone_id) REFERENCES PHONE(phone_id) 
);

CREATE TABLE PERSON_EMAIL (
    person_id INT,
    email_id INT,
    email_type VARCHAR(255),
    is_primary VARCHAR(255),

    PRIMARY KEY (person_id, email_id),
    FOREIGN KEY (person_id) REFERENCES PERSON(person_id),
    FOREIGN KEY (email_id) REFERENCES EMAIL(email_id) 
);

CREATE TABLE PROJECT_TASK (
    task_id INT PRIMARY KEY NOT NULL, 
    project_id INT,
    task_description VARCHAR(255),

    FOREIGN KEY (project_id) REFERENCES PROJECT(project_id) 
);

CREATE TABLE EMPLOYEE_PROJECT_TASK (
    employee_id INT,
    task_id INT,
    task_start_date DATE, 
    task_end_date DATE, 

    PRIMARY KEY (employee_id, task_id),
    FOREIGN KEY (employee_id) REFERENCES EMPLOYEE(employee_id),
    FOREIGN KEY (task_id) REFERENCES PROJECT_TASK(task_id) 
);

CREATE TABLE COMPANY_LOCATION (
    company_id INT,
    address_id INT, 
    location_name VARCHAR(255),

    PRIMARY KEY (company_id, address_id),
    FOREIGN KEY (company_id) REFERENCES COMPANY(company_id), 
    FOREIGN KEY (address_id) REFERENCES ADDRESS(address_id)
);

CREATE TABLE CLIENT_CONTRACT (
    client_id INT, 
    contract_id INT,
    contract_type VARCHAR(255),

    PRIMARY KEY (client_id, contract_id),
    FOREIGN KEY (client_id) REFERENCES CLIENT(client_id),
    FOREIGN KEY (contract_id) REFERENCES CONTRACT(contract_id) 
