CREATE TABLE PROJECT_CONTRACT (
    project_id INT, 
    contract_id INT, 
    start_date DATE, 
    end_date DATE,

    PRIMARY KEY (project_id, contract_id),
    FOREIGN KEY (project_id) REFERENCES PROJECT(project_id),
    FOREIGN KEY (contract_id) REFERENCES CONTRACT(contract_id)
